%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

%RdYlBu_11b (Hue fixed for protans, after ColorBrewer RdYlBu)            
%RGB                     HSV                     CMYK                            RGB256
%   0.650   0.000   0.130 348.000   1.000   0.650   0.000   0.650   0.520   0.350     165       0      33
%   0.850   0.150   0.196 356.000   0.824   0.850   0.000   0.700   0.654   0.150     216      38      50
%   0.970   0.430   0.370   6.000   0.619   0.970   0.000   0.540   0.600   0.030     247     109      94
%   1.000   0.680   0.450  25.091   0.550   1.000   0.000   0.320   0.550   0.000     255     173     114
%   1.000   0.880   0.600  42.000   0.400   1.000   0.000   0.120   0.400   0.000     255     224     153
%   1.000   1.000   0.750  60.000   0.250   1.000   0.000   0.000   0.250   0.000     255     255     191
%   0.880   1.000   1.000 180.000   0.120   1.000   0.120   0.000   0.000   0.000     224     255     255
%   0.670   0.970   1.000 185.455   0.330   1.000   0.330   0.030   0.000   0.000     170     247     255
%   0.450   0.850   1.000 196.364   0.550   1.000   0.550   0.150   0.000   0.000     114     216     255
%   0.250   0.630   1.000 209.600   0.750   1.000   0.750   0.370   0.000   0.000      63     160     255
%   0.150   0.300   1.000 229.412   0.850   1.000   0.850   0.700   0.000   0.000      38      76     255

% white
c{1}=[1,1,1];

% blue
c{2}=[0.800   0.750   1.000]; %9
c{3}=[0.650   0.930   1.000]; %7
c{4}=[0.100   0.700   1.000]; %8
c{5}=[0.400   0.300   1.000]; %10

% yellow
c{6}=[1.000   0.750   0.500]; %1
c{7}=[1.000   0.500   0.000]; %2
c{8}=[1.000   1.000   0.200]; %4

% red
c{9 }= [1.000   0.600   0.750]; %11
c{10}=[0.900   0.100   0.200]; %12
c{11}=[1,0,0];

N=length(indx);
if N~=11, error('indx should == 11'); end

for n=1:N
    if n==1, m1=1; else m1=indx(n-1)+1; end
    m2=indx(n);
for m=m1:m2
    C(m,1:3)=c{n};
end
end

